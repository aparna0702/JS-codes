// while loop

let index = 0
while (index <= 10) {
    console.log(`value of index i ${index}`);
    index++
}

let myArray = ['flash', 'batman', 'superman']
let i = 0
while (i<myArray.length) {
    console.log(`value at ${i} is ${myArray[i]}`);
    i++
}

// do while
let score = 1
do {
    console.log(`Score is ${score}`);
    score = score+2
} while (score<=15);